#include <unistd.h>
#include <stdio.h>

int	main(void/*int argc, char *argv[]*/)
{
	
	int	array[4][4];

	int	i;

	int	j;

	int	z;
	
	i = 0;
	j = 0;
	z = '1';
	
	while(i < 5)
	{
		while(j < 5)
		{
			array[i][j] = z;
			write(1, &array[i][j], 1);
			j++;
			z++;
		}
		j = 0;
		write(1, "\n", 1);
		i++;
	}
}		
